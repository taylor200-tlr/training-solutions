package literals;

public class LiteralsMain {
    public static void main(String[] args) {
        double quotient = 3/4;
        System.out.println("" + 1 + 2);
        System.out.println("1" + "2");
        System.out.println(quotient);
        quotient = 3/4D;
        System.out.println(quotient);
        long big = 3_244_444_444L;
    }

}
